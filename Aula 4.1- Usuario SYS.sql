1. Criando conexão para usuario SYS (dba) no Oracle SQL Developer

    1.1 Abra o SQL Developer

    1.2 Em Oracle Conexões clique com o botão direito e selecione Nova conexão

    Nome: sys_XEPDB1
    usuario:sys
    atribuição: sysdba
    senha:12345
    tipo conexão: basico
    nome host: localhost
    porta:1521
    selecione nome do serviço, "XEPDB1"
    Testar
    Clique em salvar
    Clique em conectar
    Insira novamente a senha do usuario

    1.3 Para desconectar, selecione a conexão no canto esquerdo da tela
        clique com botão direito e selecione desconectar
         
