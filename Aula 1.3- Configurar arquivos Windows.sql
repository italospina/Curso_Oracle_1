Configurar serviços di Windows do Java para automatico
------------------
1- Serviços
------------------
1.1- Menu iniciar
1.2- Procurar por "Serviços"
1.3- Procurar serviços do oracle "Clique em algum serviço e tecle "O""
1.4- Escolha o Serviço "OracleServiceXE" e veja se está automatico
1.5- Escolha o Serviço "OracleOraDB21Home1TNSListener" e veja se está automatico
1.6- Caso ambas não estejam automaticos definir para automatico em propriedades e tipo de inicialização
